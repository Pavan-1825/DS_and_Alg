package datastructures_algorithms;

public class TakeUserInput {

    String name;
    int age;

    public TakeUserInput() {
    }

    public TakeUserInput(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
